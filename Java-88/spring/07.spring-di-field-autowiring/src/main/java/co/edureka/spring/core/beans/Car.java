package co.edureka.spring.core.beans;

public class Car {
	private String model;
	private Float cost;
	
	public String getModel() {
		return model;
	}

	public Float getCost() {
		return cost;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public void setCost(Float cost) {
		this.cost = cost;
	}
	
}
